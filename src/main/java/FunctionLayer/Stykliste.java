package FunctionLayer;

/**
 *
 * @author Mads
 */
public class Stykliste {
    
    private int stolper;
    private int spær;
    private int søm;

    public Stykliste(int stolper) {
        this.stolper = stolper;
    }

    public int getStolper() {
        return stolper;
    }

    public void setStolper(int stolper) {
        this.stolper = stolper;
    }

    public int getSpær() {
        return spær;
    }

    public void setSpær(int spær) {
        this.spær = spær;
    }

    public int getSøm() {
        return søm;
    }

    public void setSøm(int søm) {
        this.søm = søm;
    }
    
}
